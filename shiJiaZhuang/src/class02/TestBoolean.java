package class02;
//==,>,<,!=,||,&&的用法
public class TestBoolean {
	public static void main(String[] args){
//		顶一个布尔型的变量b并赋初值true
		boolean b=true;
		int aa=1;
		System.out.println(b==false);
		System.out.println(b==true);
		System.out.println(aa>2);
		System.out.println(aa==2);
		System.out.println(aa<2);
		System.out.println(aa!=2);
		System.out.println(aa==2||aa<=2);
		System.out.println(aa==2&&aa<=2);
	}
	

}
